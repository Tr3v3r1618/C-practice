#include <stdio.h>
#include <conio.h>

int main()
{

    int i;
    int j;
    int k;

    for (i = 1; i <= 5; i++)
    {

        for (k = 1; k <= 5 - i; k++)
        {
            printf(" ");

            while (j  = 2 * i - 1);
            {

                printf("*");
                j++;
            }
        }
        printf("\n");
    }

    return 0;
}